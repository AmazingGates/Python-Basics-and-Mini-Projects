# multiple assignment - Allows us to assign mutiple variables at the same time using one line of code.

# First, this is an example of regular assignment. Example below.
name = "Brian"
age = 38
wife = "Alia"

print(name)
print(age)
print(wife)


# Now we will show an example of multiple assignment
name, age, wife = "Brian", 38, "Alia"

print(name, age, wife)


# Here is another example of multiple assignment
Brian = Alia = Bam_Bam = Mina = "Family"

print(Brian)
print(Alia)
print(Bam_Bam)
print(Mina)